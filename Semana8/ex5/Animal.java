package Semana8.ex5;

public class Animal {
    private String tipo, cor;

    public Animal() {
        this.tipo = "";
        this.cor = "";
    }

    public String getTipo() {
        return tipo;
    }

    public String getCor() {
        return cor;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }
    
    
}
